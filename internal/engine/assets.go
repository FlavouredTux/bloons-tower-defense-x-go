// helper to strip file extension
func stripExt(name string) string {
	ext := filepath.Ext(name)
	return strings.TrimSuffix(name, ext)
}

// bitmap font support

// bMFontGlyph stores metrics for one glyph in a bitmap font
type BMFontGlyph struct {
	Char   rune
	X, Y   int // position in the font atlas
	W, H   int // glyph dimensions
	Shift  int // advance width (how far to move cursor)
	Offset int // x offset when drawing
}

// bMFont represents a loaded bitmap font
type BMFont struct {
	Name   string
	Atlas  *ebiten.Image
	Glyphs map[rune]*BMFontGlyph
}

// drawText draws a string using this bitmap font at (x, y) with the given color
func (f *BMFont) DrawText(screen *ebiten.Image, text string, x, y float64, clr [3]uint8) {
	if f.Atlas == nil {
		return
	}
	dx := x
	for _, ch := range text {
		g, ok := f.Glyphs[ch]
		if !ok {
			dx += 8 // default spacing for unknown chars
			continue
		}

		// sub-image the glyph from the atlas
		rect := image.Rect(g.X, g.Y, g.X+g.W, g.Y+g.H)
		sub := f.Atlas.SubImage(rect).(*ebiten.Image)

		op := &ebiten.DrawImageOptions{}
		op.GeoM.Translate(dx+float64(g.Offset), y)
		// tint with color
		r := float32(clr[0]) / 255.0
		gr := float32(clr[1]) / 255.0
		b := float32(clr[2]) / 255.0
		op.ColorScale.Scale(r, gr, b, 1)
		screen.DrawImage(sub, op)

		dx += float64(g.Shift)
	}
}

// drawTextScaled draws text using a scale factor (useful for compact UI labels).
func (f *BMFont) DrawTextScaled(screen *ebiten.Image, text string, x, y, scale float64, clr [3]uint8) {
	if f.Atlas == nil {
		return
	}
	if scale <= 0 {
		scale = 1
	}
	dx := x
	for _, ch := range text {
		g, ok := f.Glyphs[ch]
		if !ok {
			dx += 8 * scale
			continue
		}

		rect := image.Rect(g.X, g.Y, g.X+g.W, g.Y+g.H)
		sub := f.Atlas.SubImage(rect).(*ebiten.Image)

		op := &ebiten.DrawImageOptions{}
		op.GeoM.Scale(scale, scale)
		op.GeoM.Translate(dx+float64(g.Offset)*scale, y)
		r := float32(clr[0]) / 255.0
		gr := float32(clr[1]) / 255.0
		b := float32(clr[2]) / 255.0
		op.ColorScale.Scale(r, gr, b, 1)
		screen.DrawImage(sub, op)

		dx += float64(g.Shift) * scale
	}
}

// loadBMFont loads a bitmap font from a JSON glyph definition and PNG atlas
func (am *AssetManager) LoadBMFont(name, jsonPath, pngPath string) (*BMFont, error) {
	// load the atlas image
	img, err := loadPNG(pngPath)
	if err != nil {
		return nil, fmt.Errorf("loading font atlas %s: %w", pngPath, err)
	}

	atlas := ebiten.NewImageFromImage(img)

	// parse glyph data from JSON
	type glyphJSON struct {
		Char   int `json:"char"`
		X      int `json:"x"`
		Y      int `json:"y"`
		W      int `json:"w"`
		H      int `json:"h"`
		Shift  int `json:"shift"`
		Offset int `json:"offset"`
	}
	type fontJSON struct {
		Name   string      `json:"name"`
		Size   int         `json:"size"`
		Glyphs []glyphJSON `json:"glyphs"`
	}

	data, err := os.ReadFile(jsonPath)
	if err != nil {
		return nil, fmt.Errorf("reading font JSON %s: %w", jsonPath, err)
	}
	var fd fontJSON
	if err := json.Unmarshal(data, &fd); err != nil {
		return nil, fmt.Errorf("parsing font JSON: %w", err)
	}

	font := &BMFont{
		Name:   name,
		Atlas:  atlas,
		Glyphs: make(map[rune]*BMFontGlyph),
	}

	for _, g := range fd.Glyphs {
		font.Glyphs[rune(g.Char)] = &BMFontGlyph{
			Char:   rune(g.Char),
			X:      g.X,
			Y:      g.Y,
			W:      g.W,
			H:      g.H,
			Shift:  g.Shift,
			Offset: g.Offset,
		}
	}

	fmt.Printf("Loaded bitmap font '%s': %d glyphs\n", name, len(font.Glyphs))
	return font, nil
}
